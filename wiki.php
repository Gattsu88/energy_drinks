        <h2 id="demographics">Demographics</h2>

        <p>Globally, energy drinks are typically attractive to young people. Approximately 66 percent of consumers are between the ages of 13 and 35 years, with
        males being approximately 65 percent of the market. A 2008 statewide Patient Poll conducted by the Pennsylvania Medical Society's Institute for
        Good Medicine found that: 20 percent of respondents aged between 21 and 30 had used energy drinks in high school or college to stay awake longer to
        study, or to write a paper; and 70 percent of respondents knew someone who had used an energy drink to stay awake longer to study or work.</p>

        <p>US research by Packaged Facts in 2012 showed that consumers aged between 18 and 34 years, men, Hispanics, Pacific region residents and adults with
        children in the household were the demographic groups that were using the highest amounts of energy drinks.</p>
