        <p>A health warning on a can of the Austrian Power Horse energy drink: "Consumption of more than two cans in a day may be harmful to your health. Not to be
        used for pregnant women, breast feeders, children under the age of 16, people with heart disease, high blood pressure, diabetes, allergy to caffeine,
        and athletes during exercise." Energy drinks have the effects caffeine and sugar provide, but there is little or no evidence that the wide variety of
        other ingredients have any effect. Most of the effects of energy drinks on cognitive performance, such as increased attention and reaction speed, are
        primarily due to the presence of caffeine. Advertising for energy drinks usually features increased muscle strength and endurance, but there is little
        evidence to support this in the scientific literature.</p>

        <p>A caffeine intake of 400 mg per day is considered as safe from the European Food Safety Authority (EFSA). Adverse effects associated with caffeine
        consumption in amounts greater than 400 mg include nervousness, irritability, sleeplessness, increased urination, abnormal heart rhythms (arrhythmia),
        and dyspepsia. Consumption also has been known to cause pupil dilation when taken with certain antidepressants or SSRIs. Caffeine dosage is not required
        to be on the product label for food in the United States, unlike drugs, but most (although not all) place the caffeine content of their drinks on the
        label anyway, and some advocates are urging the FDA to change this practice.</p>

        <h3>With alcohol</h3>

        <p>Combined use of caffeine and alcohol may increase the rate of alcohol-related injury. Energy drinks can mask the influence of alcohol, and a person
        may misinterpret their actual level of intoxication. Since caffeine and alcohol are both diuretics, combined use increases the risk of dehydration,
        and the mixture of a stimulant (caffeine) and depressant (alcohol) sends contradictory messages to the nervous system and can lead to increased heart
        rate and palpitations. Although people decide to drink energy drinks with alcohol with the intent of counteracting alcohol intoxication, many others
        do so to hide the taste of alcohol. However, in the 2015, the EFSA concluded, that “Consumption of other constituents of energy drinks at
        concentrations commonly present in such beverages would not affect the safety of single doses of caffeine up to 200 mg.” Also the consumption of alcohol,
        leading to a blood alcohol content of about 0.08%, would, according to the EFSA, not affect the safety of single doses of caffeine up to 200 mg. Up to
        these levels of intake, caffeine is unlikely to mask the subjective perception of alcohol intoxication.</p>

        <h3>Health problems</h3>

        <p>Excessive or repeated consumption of energy drinks can lead to cardiac problems, such as arrhythmias and heart attacks, and psychiatric conditions such
        as anxiety and phobias. In Europe, energy drinks containing taurine and caffeine have been associated with the deaths of athletes.
        A 2017 study in the Journal of the American Heart Association noted that caffeine content was not the only factor, and that the cocktail of other
        ingredients in energy drinks made them more dangerous than drinks whose only stimulant was caffeine; the study noted that more research was needed on
        the individual ingredients to determine which ones pose a hazard.</p>

        <p>Research suggests that emergency room visits are on the increase. In 2005, there were 1,494 emergency room visits related to energy drink consumption
        in the United States; whereas, in 2011, energy drinks were linked to 20,783 emergency room visits. During this period of increase, males consumers
        consistently had a higher likelihood of visiting the emergency room over their female counterparts. Research trends also show that emergency room
        visits are caused mainly by adverse reactions to the drinks. In 2011, there were 14,042 energy drink-related hospital visits. Misuse and abuse of
        these caffeinated beverages also cause a significant amount of emergency room visits. By 2011, there were 6,090 visits to the ER due to misuse/abuse of
        the drinks. In many cases 42% of patients had mixed energy drinks with another stimulant, and in the other 58% of cases the energy drink was the
        only thing that had been consumed. Several studies suggest that energy drinks may be a gateway drug.</p>
