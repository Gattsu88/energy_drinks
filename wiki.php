        <p>Market research firm Euromonitor calculated that the global energy drink market was worth US$3.8 billion in 1999 and this value grew to US$27.5 billion
        in 2013.</p>

        <p>In 2000, the US energy drink market was worth US$350 million and data from the Packaged Facts company shows that the industry grew by 60 percent between
        2008 and 2012 in the US—by 2012 total US sales were over US$12.5 Billion. Red Bull and Monster were the two best-selling brands in 2012, accounting for
        nearly 80% of US energy drink sales, and the energy shot market is worth over US$1 billion in 2014.</p>

        <h3>Regulation</h3>

        <p>The energy drink Red Bull did not get market approval in France after the death of an 18-year-old Irish athlete, Ross Cooney, who died within hours after
        playing a basketball game and consuming four cans of the product. This ban was challenged in the European Court of Justice in 2004 and consequently
        lifted. Norway did not allow Red Bull for a time, although this restriction has recently been relaxed. In May 2009 it became legal to sell in Norway.
        The Norwegian version has reduced B-vitamin B6. The United Kingdom investigated the drink, but only issued a warning against its consumption by
        children and pregnant women.</p>

        <p>In 2009 under the Ministry of Social Protection, Colombia prohibited the sale and commercialization of energy drinks to minors under the age of 14.</p>

        <p>In November 2012, President Ramzan Kadyrov of Chechnya (Russian Federation) ordered his government to develop a bill banning the sale of energy drinks,
        arguing that as a form of "intoxicating drug", such drinks were "unacceptable in a Muslim society". Kadyrov cited reports of one death and 530 hospital
        admissions in 2012 due to "poisoning" from the consumption of such drinks. A similar view was expressed by Gennady Onishchenko, Chief Sanitary Inspector
        of Russia.</p>

        <p>In 2009, a school in Hove, England requested that local shops refrain from selling energy drinks to students. Headteacher Malvina Sanders added that
        "This was a preventative measure, as all research shows that consuming high-energy drinks can have a detrimental impact on the ability of young people
        to concentrate in class." The school negotiated for their local branch of the Tesco supermarket to display posters asking students not to purchase the
        products. Similar measures were taken by a school in Oxted, England, which banned students from consuming drinks and sent letters to parents.</p>

        <p>Some countries have certain restrictions on the sale and manufacture of energy drinks. In Australia and New Zealand, energy drinks are regulated under
        the Australia New Zealand Food Standards Code; limiting the caffeine content of 'formulated caffeinated beverages' (energy drinks) at 320 mg/L
        (9.46 mg/oz) and soft-drinks at 145 mg/L (4.29 mg/oz). Mandatory caffeine labeling is issued for all food products containing guarana in the country,
        and Australian energy drink labels warn consumers to drink no more than two cans per day.</p>

        <p>As of 2013 in the United States some energy drinks, including Monster Energy and Rockstar Energy, were reported to be rebranding their products as
        beverages rather than as dietary supplements. As beverages they would be relieved of F.D.A. reporting requirements with respect to deaths and injuries
        and can be purchased with food stamps, but must list ingredients on the can.</p>

        <p>In June 2016, Latvia banned the sale of energy drinks containing caffeine or stimulants like taurine and guarana to people under the age of 18. In January
        2018, many United Kingdom supermarkets banned the sale of energy drinks containing more than 150 mg of caffeine per liter to people under 16 years old.</p>

        <h3>Ban on caffeinated alcoholic beverages</h3>

        <p>Some places ban the sale of prepackaged caffeinated alcoholic beverages, which can be described as energy drinks containing alcohol. In response to
        these bans, the marketers can change the formula of their products.</p>
