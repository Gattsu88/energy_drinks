        <p>An energy drink is a type of beverage containing stimulant drugs, usually including caffeine, which is marketed as providing mental and physical
        stimulation (marketed as "energy", but distinct from food energy). They may or may not be carbonated and many also contain sugar or other sweeteners,
        herbal extracts, taurine, and amino acids. They are a subset of the larger group of energy products, which includes bars and gels, and distinct from
        sports drinks, which are advertised to enhance sports performance. There are many brands and varieties in this beverage category.</p>

        <p>Coffee, tea and other naturally caffeinated beverages are usually not considered energy drinks. Other soft drinks such as cola may contain caffeine,
        but are also not energy drinks. Some alcoholic beverages, such as Buckfast Tonic Wine, contain caffeine and other stimulants. According to the
        Mayo Clinic, it is safe for the typical healthy adult to consume a total of 400 mg of caffeine a day. This has been confirmed by a panel of the
        European Food Safety Authority, which also concludes that a caffeine intake of up to 400 mg per day does not raise safety concerns for adults.
        According to the ESFA this is equivalent to 4 cups of coffee (90 mg each) or 5 standard cans (250 ml) of energy drink (80 mg each).</p>

        <p>Energy drinks have the effects caffeine and sugar provide, but there is little or no evidence that the wide variety of other ingredients have any effect.
        Most of the effects of energy drinks on cognitive performance, such as increased attention and reaction speed, are primarily due to the presence of
        caffeine. Other studies, however, ascribe those performance improvements to the effects of the combined ingredients. Advertising for energy drinks
        usually features increased muscle strength and endurance, but there is still no scientific consensus to support these statements. Energy drinks have
        been associated with health risks, such as an increased rate of alcohol-related injury, and excessive or repeated consumption can lead to cardiac
        and psychiatric conditions.</p>
