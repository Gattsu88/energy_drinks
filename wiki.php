        <h1 id="wiki">Energy drinks</h1>

        <figure>
            <img class="img-thumbnail" src="images/cans.jpg" alt="Effects">
            <figcaption>A health warning on a can of the Austrian Power Horse energy drink: "Consumption of more than two cans in a day may be harmful to your health.
            Not to be used for pregnant women, breast feeders, children under the age of 16, people with heart disease, high blood pressure, diabetes,
