    <div class="row">
        <h2 id="uses">Uses</h2>

        <p>Energy drinks are marketed to provide the benefits among health effects of caffeine along with benefits from the other ingredients they contain.
        Health experts agree that energy drinks which contain caffeine do improve alertness. The consumption of alcohol drinks combined with energy drinks
        is a common occurrence on many college campuses. The alcohol industry has recently been criticized for marketing cohesiveness of alcohol and energy
        drinks. The combination of the two in college students is correlated to students experiencing alcohol-related consequences, and several health risks.</p>

        <p>There is no good evidence that other ingredients in energy drinks provide further benefits, even though the drinks are frequently advertised in a way
        that suggests they have unique benefits. The dietary supplements in energy drinks may be purported to provide produce benefits, such as for
        vitamin B12, but no claims of using supplements to enhance health in otherwise normal people have been verified scientifically. Various marketing
        organizations have described energy drinks by saying their beverage "gives you wings", is "scientifically formulated", or is a "killer energy brew".
        Marketing of energy drinks has been particularly directed towards teenagers, with manufacturers sponsoring or advertising at extreme sports events and
        music concerts, and targeting a youthful audience through social media channels.</p>

        <p>When mixed with alcohol, either as a prepackaged caffeinated alcoholic drink, a mixed drink, or just a beverage consumed around the same time as
        alcohol, energy drinks are often consumed in social settings.</p>
