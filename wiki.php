        <h2 id="variants">Variants</h2>

        <h3>Energy shots</h3>

        <p>Energy shots are a specialized kind of energy drink. Whereas most energy drinks are sold in cans or bottles, energy shots are usually sold in smaller
        50ml bottles. Energy shots can contain the same total amount of caffeine, vitamins or other functional ingredients as their larger versions, and may be
        considered concentrated forms of energy drinks. The marketing of energy shots generally focuses on their convenience and availability as a low-calorie
        "instant" energy drink that can be taken in one swallow (or "shot"), as opposed to energy drinks that encourage users to drink an entire can, which may
        contain 250 calories or more.</p>

        <h3>Caffeinated alcoholic drink</h3>

        <p>Energy drinks such as Red Bull are often used as mixers with alcoholic beverages, producing mixed drinks such as Vodka Red Bull which are similar to but
        stronger than rum and coke with respect to the amount of caffeine that they contain. Sometimes this is configured as a bomb shot, such as the
        Jägerbomb or the F-Bomb — Fireball Cinnamon Whisky and Red Bull.</p>

        <p>Caffeinated alcoholic drinks are also sold in some countries in a wide variety of formulations. The American products Four Loko and Joose originally
        combined caffeine and alcohol before caffeinated alcoholic beverages were banned in the U.S. in 2010.</p>

        <h3>Relaxation drinks</h3>

        <p>Several beverages have been marketed in the 2000s as "anti-energy", "chill out", or "relaxation" drinks, including Lava Cola, Slow Cow, Drank, Marley's
        Mellow Mood, Mary Jane's Relaxing Soda, Chill, Calm, Malava Kava, V.i.B., Take 5, Relax by Rockstar and Jones Gaba. They are growing in popularity, with
        sales doubling from 2008 to 2010, and expected to more than double again by 2014. They contain ingredients such as theanine and melatonin.</p>
