        <h2 id="chemistry">Chemistry</h2>

        <figure>
            <img class="img-thumbnail" src="images/facts.jpg" alt="Facts">
            <figcaption>A Nutrition facts label for an energy drink.</figcaption>
        </figure>

        <p>A Nutrition facts label for an energy drink.
        Energy drinks generally contain methylxanthines (including caffeine), B vitamins, carbonated water, and high-fructose corn syrup (for non-diet versions).
        Other commonly used ingredients are guarana, yerba mate, açaí, and taurine, plus various forms of ginseng, maltodextrin, inositol, carnitine, creatine,
        glucuronolactone, sucralose and ginkgo biloba.</p>

        <p>Some energy drink manufacturers do not report how much caffeine their products contain. One survey found that various energy drinks had 6–242
        milligrams (mg) of caffeine per serving. In comparison, an 8 US fluid ounces (240 ml) cup of coffee contains 100 mg of caffeine and 12 US fluid
        ounces (350 ml) of Coca-Cola Classic contains 35 mg of caffeine. Consumer Reports expected that the caffeine content of a beverage should be
        within 20% of what the label claims, but product testing on United States energy drinks found that the caffeine levels of some energy drinks vary beyond
        that range. The Monster Beverage Corporation explained they do not report caffeine levels because "there is no legal or commercial business
        requirement to do so, and also because our products are completely safe, and the actual numbers are not meaningful to most consumers." Scientific groups
        and consumer organizations have requested more disclosure in ingredient lists.</p>

        <p>The sugar in non-diet energy drinks is food energy that can be utilized by the human body.</p>
