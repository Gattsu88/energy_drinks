        <h2 id="history">History</h2>

        <p>Energy drinks were an active subset of the early soft drink industry; Pepsi, for instance, was originally marketed as an energy booster. Coca-Cola's
        name was derived from its two active ingredients, both known stimulants: coca leaves and kola nuts (a source of caffeine). Fresh coca leaves were
        replaced by "spent" ones in 1904 because of concerns over the use of cocaine in food products; the federal lawsuit United States v. Forty Barrels
        and Twenty Kegs of Coca-Cola pressured The Coca-Cola Company into reducing the amount of caffeine in its formula by 1916. These developments brought
        an end to the first wave of energy drinks.</p>

        <p>In the UK, Lucozade Energy was originally introduced in 1929 as a hospital drink for "aiding the recovery;" in the early 1980s, it was promoted as an
        energy drink for "replenishing lost energy." One of the first post-Forty Barrels energy drinks introduced in America was Dr. Enuf. Its origins date
        back to 1949, when a Chicago businessman named William Mark Swartz was urged by coworkers to formulate a soft drink fortified with vitamins as an
        alternative to sugar sodas full of empty calories. He developed an "energy booster" drink containing B vitamins, caffeine and cane sugar. After placing
        a notice in a trade magazine seeking a bottler, he formed a partnership with Charles Gordon of Tri-City Beverage to produce and distribute the soda.
        Dr. Enuf is still being manufactured in Johnson City, Tennessee and sold sparsely throughout the nation.</p>

        <p>In Japan, the energy drink dates at least as far back as the early 1960s, with the launch of the Lipovitan brand. However, in Japan, most of the
        products of this kind bear little resemblance to soft drinks, and are sold instead in small brown glass medicine bottles, or cans styled to resemble
        such containers. These "eiyō dorinku" (literally, "nutritional drinks") are marketed primarily to salarymen. Bacchus-F, a South Korean drink closely
        modeled after Lipovitan, also appeared in the early 1960s and targets a similar demographic.</p>

        <p>In 1985, Jolt Cola was introduced in the United States. Its marketing strategy centered on the drink's caffeine content, billing it as a means to
        promote wakefulness. The drink's initial slogan read: "All the sugar and twice the caffeine."</p>

        <p>In 1995, PepsiCo launched Josta, the first energy drink introduced by a major US beverage company (one that had interests outside energy drinks),
        but Pepsi discontinued the product in 1999. Pepsi would later return to the energy drink market with the AMP brand.</p>

        <p>In Europe, energy drinks were pioneered by the Lisa company and a product named "Power Horse", before Dietrich Mateschitz, an Austrian entrepreneur,
        introduced the Red Bull product, a worldwide bestseller in the 21st century. Mateschitz developed Red Bull based on the Thai drink Krating Daeng,
        itself based on Lipovitan. Red Bull became the dominant brand in the US after its introduction in 1997, with a market share of approximately 47% in 2005.</p>

        <p>In New Zealand and Australia, the leading energy drink product in those markets, V, was introduced by Frucor Beverages. The product now represents over
        60% of market in New Zealand and Australia.</p>

        <p>UK supermarkets have launched their own brands of energy drinks, sold at lower prices than the major soft drink manufacturers, that are mostly produced
        by Canadian beverage maker Cott. Tesco supermarkets sell "Kx" (formerly known as "Kick"), Sainsbury's sell "Blue Bolt" and Asda sell "Blue Charge"—all
        three drinks are sold in 250-milliliter cans and 1-liter bottles—while Morrison's sell "Source" in 250-milliliter cans. Cott sells a variety of other
        branded energy drinks to independent retailers in various containers.</p>

        <p>Since 2002, there has been a growing trend for packaging energy drinks in bigger cans. In many countries, including the US and Canada,
        there is a limitation on the maximum caffeine per serving in energy drinks, so manufacturers include a greater amount of caffeine by including multiple
        servings per container. Popular brands such as Red Bull, Hype Energy Drinks and Monster have increased the can size.</p>

        <p>The energy shot product, an offshoot of the energy drink, was launched in the US with products such as "5-Hour Energy," which was first released onto
        the market in 2004. A consumer health analyst explained in a March 2014 media article: "Energy shots took off because of energy drinks. If you’re a white
        collar worker, you’re not necessarily willing to down a big Monster energy drink, but you may drink an energy shot."</p>

        <p>In 2007, energy drink powders and effervescent tablets were introduced, whereby either can be added to water to create an energy drink.</p>

        <p>Energy drinks are also popular as drink mixers—Red Bull and vodka is a popular combination. In the US, a product called "Four Loko" formerly mixed beer
        with caffeine, while Kahlua is a coffee-flavored alcoholic beverage.</p>

        <p>On 14 August 2012, the word "energy drink" was listed for the first time in the Merriam-Webster's Collegiate Dictionary.</p>
